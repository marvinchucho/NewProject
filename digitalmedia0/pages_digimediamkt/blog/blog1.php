        <div class="texto">
            <p>Hoy en día es impensable imaginar nuestras vidas sin internet; y no solo hablamos a nivel social sino también a nivel académico y laboral. Según datos de la consultora Comscore se estima que un promedio de 12 millones y medio de personas están
                conectadas activamente en el Perú; de los cuáles un 82% lo hace mediante teléfono celular. </p>

            <p>Estas cifras son contundentes al momento de analizar la dependencia que tenemos del internet como sociedad, ya que está presente constantemente en nuestras actividades cotidianas. Hoy en día con el contexto de pandemia mundial, la digitalización
                ya no solo es evidente sino necesaria para mantener activas nuestras relaciones familiares, amicales, laborales y empresariales. Dada esta circunstancia, es preciso que los negocios empiecen a ver formas para mantener activas sus operaciones;
                una de las alternativas para que sigan obteniendo utilidades, sin poner en riesgo la integridad de las personas, es emplear estrategias de marketing digital. Esta herramienta no solo puede ser empleada por grandes corporaciones, sino también
                por las pequeñas – medianas empresas (PYMES) y emprendimientos. A continuación, te explicamos las razones que tiene un negocio para empezar a trabajar estrategias de marketing digital:</p>

            <h2>ECÓNOMICO</h2>

            <p>A diferencia de la publicidad tradicional en radio, televisión, anuncios en periódicos o material impresos para repartir y colocar en las calles, los cuáles suponían una gran inversión de dinero y tiempo (en el caso de repartir volantes o
                colocar afiches en las calles); la publicidad en internet y redes sociales te permite invertir desde diez soles desde la comodidad de tu casa u oficina para poder llegar a cientos o miles de clientes potenciales. Recuerda que mientras
                más inviertas, mayores serán las chances de poder llegar a más clientes potenciales y cerrar ventas para tu negocio.</p>

            <h2>IMPACTO</h2>

            <p>Como mencionamos anteriormente, la publicidad en internet te permite llegar a miles de personas en solo unos minutos. Según Fireclick Index, la tasa de conversión de clientes contactados a través de canales de canales digitales es del 4%.
                Es decir, que si realizas una inversión de 25 soles, la cual te permite llegar a mil personas, eso supondría que cerrarías en promedio 40 ventas.</p>

            <h2>INTERACTIVO</h2>

            <p>Permite comunicarte constantemente con tus clientes actuales y con el público potencial que puede interesarse por tu producto o servicio. De esta manera, podrás estar en contacto permanente con ellos, conocer sus necesidades, inquietudes y
                brindarle soluciones en base a los productos que tu fabriques. Recuerda, que en este punto es importante tener una base de datos con información de tus clientes actuales y potenciales.</p>

            <h2>MEDIBLE</h2>

            <p>Las plataformas como Facebook o Instagram te permiten dirigir tu publicidad a personas que realmente pueden interesarse en tu producto; es decir, a diferencia de la publicidad tradicional en la que repartías volantes a cualquier persona sin
                saber si estaba realmente interesada en hacerte una compra, las redes sociales brindan herramientas que permitirán a tu publicidad enfocarse en verdaderos compradores potenciales. Así mismo, te permite medir los resultados de todas las
                campañas que realices; por ejemplo, si colocas un anuncio en alguna avenida de Lima, no sabes cuanta gente llega a verlo o cuantos anotaron el número de contacto que dejaste en esa valla ni cuantas ventas realizaste como consecuencia de
                dicha publicidad; sin embargo, la publicidad en internet te permite saber cuántas personas ven el anuncio, cuántas se contactan contigo y cuántas ventas registras como resultado de la campaña.</p>

            <h2>RENTABLE</h2>

            <p>Como hemos visto en este artículo, las estrategias de marketing digital te permiten contactar a una gran cantidad de compradores, cerrar varias ventas en el menor tiempo posible y con una inversión mínima. Por ello, si gestionas adecuadamente
                tus redes sociales y/o página web los resultados económicos serán verán en el corto plazo y con márgenes de ganancia bastante amplios.</p>
